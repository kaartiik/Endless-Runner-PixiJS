export function LDGROUP1(grid) {
  const laserDroidsArr = [];

  const ld1 = {
    y: grid.matrix[2][0].y,
  };

  laserDroidsArr.push(ld1);

  const ld2 = {
    y: grid.matrix[14][0].y,
  };

  laserDroidsArr.push(ld2);

  return laserDroidsArr;
}

